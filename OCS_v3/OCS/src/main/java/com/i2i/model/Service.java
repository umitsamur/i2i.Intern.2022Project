package com.i2i.model;

public class Service {
    private int regex;
    private int round;
    private double price;

    public int getRegex() {
        return regex;
    }

    public void setRegex(int regex) {
        this.regex = regex;
    }

    public int getRound() {
        return round;
    }

    public void setRound(int round) {
        this.round = round;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
